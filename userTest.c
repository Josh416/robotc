/*This is a set of movement functions.  Goals: make the robot go forward, backwards, turn,
and go to the side.  */
#include "fourWheelMovement.c"
#include "rpgCommon.c"
#include "MovementCommon.c"

#define deadZone 10

task main()
{
	while(true)
	{
		getJoystickSettings(joystick);  // Update Buttons and Joysticks

		wait1Msec(1);

		/*--------------------------
		controller one
		-------------------------*/
		/*-------------------------
		maping
		---------------------------
		up =
		down =
		left = strafe left
		right = strafe right
		joystick left = left motors
		joystick right = right motors
		A = catcher down
		B =	catcher up
		X =
		Y =
		L1 =
		L2 =
		R1 =
		R2 =
		---------------------------*/


		if((abs(joystick.joy1_y1) >= deadZone) || (abs(joystick.joy1_y2) >= deadZone))
		{
			setMotion(joystick.joy1_y1, joystick.joy1_y2);
		}
		else if(joystick.joy1_TopHat == 6)
		{
			strafe(50);
		}
		else if(joystick.joy1_TopHat == 2)
		{
			strafe(-50);
		}
		else if(joy1Btn(JOY_BUTTON_A))
		{
			int iCRate = servoChangeRate[goalCapture];	// Save change rate
			servoChangeRate[goalCapture] = 0; 					// Max Speed
			servo[goalCapture] = 66;					// Set servo position
			wait1Msec(20);
			servoChangeRate[goalCapture] = iCRate;			// Reset the servo
		}
		else if(joy1Btn(JOY_BUTTON_B))
		{
			int iCRate = servoChangeRate[goalCapture];	// Save change rate
			servoChangeRate[goalCapture] = 0; 					// Max Speed
			servo[goalCapture] = 200;					// Set servo position
			wait1Msec(20);
			servoChangeRate[goalCapture] = iCRate;			// Reset the servo
		}
		else
		{
			stopMotors();
		}

		/*--------------------------
		controller two
		-------------------------*/
		/*-------------------------
		maping
		---------------------------
		up =
		down =
		left =
		right =
		joystick left =
		joystick right =
		A =
		B =
		X =
		Y =
		LB = big backward
		LT = big forward
		RB = small backward
		RT = small forward
		---------------------------*/

		if(joy2btn(JOY_BUTTON_LB))
		{
			motor[leftCollecter] = -100;
		}

		else if(joy2btn(JOY_BUTTON_LT))
		{
			motor[leftCollecter] = 100;
		}

		else if(joy2btn(JOY_BUTTON_RB))
		{
			motor[rightCollecter] = 100;
		}

		else if(joy2btn(JOY_BUTTON_RT))
		{
			motor[rightCollecter] = -100;
		}

		else
		{
			stop2ndMotors();
		}
	}
}
